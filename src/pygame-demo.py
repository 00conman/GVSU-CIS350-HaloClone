  done = False
  while not done:
    # Handle user input
    for event in pygame.event.get():
      if event.type == pygame.QUIT: # quit pressed
        done = True

      elif event.type == pygame.KEYUP: # key released
        if event.key == pygame.K_ESCAPE:
          done = True

        # Movement keys
        elif event.key == pygame.K_RIGHT:
          if checkValidMove(player_col+1,player_row):
            player_col += 1
        elif event.key == pygame.K_LEFT:
          if checkValidMove(player_col-1,player_row):
            player_col -= 1
        elif event.key == pygame.K_UP:
          if checkValidMove(player_col,player_row-1):
            player_row -= 1
        elif event.key == pygame.K_DOWN:
          if checkValidMove(player_col,player_row+1):
            player_row += 1



    # Draw surfaces
    window.fill(BLACK)
    drawMap()
    drawPlayer(player_col, player_row)
    pygame.display.update()







