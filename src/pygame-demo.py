def drawMap():
  x = 0
  y = 0
  for j in range(len(game_map)):
    for i in range(len(game_map[j])):
      if game_map[j][i] == 0:
        pygame.draw.rect(window, GRAY, (x, y, TILE_WIDTH, TILE_HEIGHT))
      elif game_map[j][i] == 1:
        pygame.draw.rect(window, BLUE, (x, y, TILE_WIDTH, TILE_HEIGHT))
      elif game_map[j][i] == 2:
        pygame.draw.rect(window, PLUM, (x, y, TILE_WIDTH, TILE_HEIGHT))
       
      x += TILE_WIDTH
    y += TILE_HEIGHT
    x = 0

def checkValidMove(new_col, new_row):
  if new_row >= 0 and new_row < (len(game_map))    and \
     new_col >= 0 and new_col < (len(game_map[0])) and \
     game_map[new_row][new_col] == 0:
    return True
  return False

def drawPlayer(col,row):
  pygame.draw.rect(window, GREEN, (col*TILE_WIDTH, row*TILE_HEIGHT, TILE_WIDTH, TILE_HEIGHT))


